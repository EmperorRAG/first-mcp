/**
 * Content hooks plugin — injects header comment and footer on every page.
 *
 * @param {import('typedoc-plugin-markdown').MarkdownApplication} app
 */
export function load(app) {
	app.renderer.markdownHooks.on("page.begin", () => {
		return "<!-- Generated by TypeDoc — do not edit -->\n";
	});

	app.renderer.markdownHooks.on("page.end", () => {
		return "\n---\n\n*Auto-generated API documentation — [source](https://github.com/EmperorRAG/first-mcp)*\n";
	});
}
